import { amoledLightTheme } from './themeDefinitions/amoledLightTheme';
import { registerImportedTheme } from './registerImportedTheme';
import type { ImportedTheme } from '../../types';

export function registerBlinkThemes(monaco: any, importedTheme?: ImportedTheme | null): void {
  defineBlinkTheme(monaco, 'blinkcode-dark', darkTheme);
  defineBlinkTheme(monaco, 'blinkcode-light', lightTheme);
  defineBlinkTheme(monaco, 'blinkcode-tokyonight', tokyonightTheme);
  defineBlinkTheme(monaco, 'blinkcode-tokyonight-light', tokyonightLightTheme);
  defineBlinkTheme(monaco, 'blinkcode-everforest', everforestTheme);
  defineBlinkTheme(monaco, 'blinkcode-everforest-light', everforestLightTheme);
  defineBlinkTheme(monaco, 'blinkcode-ayu', ayuTheme);
  defineBlinkTheme(monaco, 'blinkcode-ayu-light', ayuLightTheme);
  defineBlinkTheme(monaco, 'blinkcode-catppuccin', catppuccinTheme);
  defineBlinkTheme(monaco, 'blinkcode-catppuccin-light', catppuccinLightTheme);
  defineBlinkTheme(monaco, 'blinkcode-catppuccin-macchiato', catppuccinMacchiatoTheme);
  defineBlinkTheme(monaco, 'blinkcode-catppuccin-macchiato-light', catppuccinMacchiatoLightTheme);
  defineBlinkTheme(monaco, 'blinkcode-gruvbox', gruvboxTheme);
  defineBlinkTheme(monaco, 'blinkcode-gruvbox-light', gruvboxLightTheme);
  defineBlinkTheme(monaco, 'blinkcode-kanagawa', kanagawaTheme);
  defineBlinkTheme(monaco, 'blinkcode-kanagawa-light', kanagawaLightTheme);
  defineBlinkTheme(monaco, 'blinkcode-nord', nordTheme);
  defineBlinkTheme(monaco, 'blinkcode-nord-light', nordLightTheme);
  defineBlinkTheme(monaco, 'blinkcode-matrix', matrixTheme);
  defineBlinkTheme(monaco, 'blinkcode-matrix-light', matrixLightTheme);
  defineBlinkTheme(monaco, 'blinkcode-one-dark', oneDarkTheme);
  defineBlinkTheme(monaco, 'blinkcode-one-dark-light', oneDarkLightTheme);
  defineBlinkTheme(monaco, 'blinkcode-amoled', amoledTheme);
  defineBlinkTheme(monaco, 'blinkcode-amoled-light', amoledLightTheme);
  registerImportedTheme(monaco, importedTheme || null);
}
