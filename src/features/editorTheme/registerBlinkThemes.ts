import { tokyonightLightTheme } from './themeDefinitions/tokyonightLightTheme';
import { everforestTheme } from './themeDefinitions/everforestTheme';
import { everforestLightTheme } from './themeDefinitions/everforestLightTheme';
import { ayuTheme } from './themeDefinitions/ayuTheme';
import { ayuLightTheme } from './themeDefinitions/ayuLightTheme';
import { catppuccinTheme } from './themeDefinitions/catppuccinTheme';
import { catppuccinLightTheme } from './themeDefinitions/catppuccinLightTheme';
import { catppuccinMacchiatoTheme } from './themeDefinitions/catppuccinMacchiatoTheme';
import { catppuccinMacchiatoLightTheme } from './themeDefinitions/catppuccinMacchiatoLightTheme';
import { gruvboxTheme } from './themeDefinitions/gruvboxTheme';
import { gruvboxLightTheme } from './themeDefinitions/gruvboxLightTheme';
import { kanagawaTheme } from './themeDefinitions/kanagawaTheme';
import { kanagawaLightTheme } from './themeDefinitions/kanagawaLightTheme';
import { nordTheme } from './themeDefinitions/nordTheme';
import { nordLightTheme } from './themeDefinitions/nordLightTheme';
import { matrixTheme } from './themeDefinitions/matrixTheme';
import { matrixLightTheme } from './themeDefinitions/matrixLightTheme';
import { oneDarkTheme } from './themeDefinitions/oneDarkTheme';
import { oneDarkLightTheme } from './themeDefinitions/oneDarkLightTheme';
